    private void insertarBotActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertarBotActionPerformed
        
        String competencia = nombreCompeText.getText();
        
        if(PrincipalAtletismo.claseMain.getCompetencia(competencia) == null){
            
            JOptionPane.showMessageDialog(this, "Esta competencia no est치 registrada");
            return;
        }
        
        String idAtleta = idAtletaText2.getText();
        
        if(PrincipalAtletismo.claseMain.getAtleta(idAtleta) == null){
            
            JOptionPane.showMessageDialog(this, "Este atleta no est치 registrado");
            return;
        }
        
        String nombrePrueba = nombrePruebaText1.getText();
        
        if(PrincipalAtletismo.claseMain.validarCompetenciasPruebas(competencia, nombrePrueba) == null){
            
             JOptionPane.showMessageDialog(this, "Este atleta no est치 registrado");
            return;
        }
        
        PrincipalAtletismo.claseMain.setAtletaCompetenciaPrueba(idAtleta, nombrePrueba, competencia);
        
      
        this.dispose();
        CompetenciaPantallaAgregaAtleta AgregaP = new CompetenciaPantallaAgregaAtleta();//menu prinicpal de competencia
        AgregaP.setVisible(true);
    }//GEN-LAST:event_insertarBotActionPerformed

