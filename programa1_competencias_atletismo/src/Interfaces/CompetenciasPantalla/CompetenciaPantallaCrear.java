    private void agregarBotActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarBotActionPerformed
        // TODO add your handling code here:
        String nombre = nombreComText.getText();
        String pais = paisComText.getText();
        String lugar = lugarComText.getText();
        String fechaInicial = fecInicialText.getText();
        String fechaFinal = fecFinaltext.getText();
        String setCompetencia;
       
        if(PrincipalAtletismo.claseMain.getCompetencia(nombre) != null){
            
            setCompetencia = PrincipalAtletismo.claseMain.modificarCompetencia(nombre, lugar, pais, fechaFinal, fechaFinal);
            
            if(setCompetencia != null){
                
                JOptionPane.showMessageDialog(this, setCompetencia);
                return;
            }
        
        }
        else{
            
            setCompetencia = PrincipalAtletismo.claseMain.setCompetencia(nombre, pais, lugar, fechaInicial, fechaFinal);
            if(setCompetencia != null){
            
                JOptionPane.showMessageDialog(this, setCompetencia);
                return;
            }
        
        }
            
        this.dispose();
        CompetenciaPantalla CompeP = new CompetenciaPantalla();//menu prinicpal de competencia
        CompeP.setVisible(true);
        
      
    }//GEN-LAST:event_agregarBotActionPerformed

